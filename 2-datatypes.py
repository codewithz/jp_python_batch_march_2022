# Sequence
# List - [] - mutable
# Tuple - () - readonly list - immutable
# Range - range(from,to) - iterable

x=['Hyundai','Honda','Tata','Ford']
print(x)
print(type(x))

x=('Hyundai','Honda','Tata','Ford')
print(x)
print(type(x))

x=range(1,10)
print(x)
print(type(x))

# Set and Dictionary

